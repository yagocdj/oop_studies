import javax.swing.JOptionPane;

public class Friend02 {
	public static void main(String[] args) {

		String name1 = JOptionPane.showInputDialog("What's your name?");
		String name2 = JOptionPane.showInputDialog(name1 + ", who is your friend?");
		
		JOptionPane.showMessageDialog(null, name1 + " is friend of " + name2 + ".");
		
	}
}
